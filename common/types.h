/**
 * @file types.h
 * @brief DNS 协议相关类型定义 —— RFC 1035 标准的记录类型、类与响应码枚举
 */

#pragma once
#include <cstdint>
#include <string>

// 撤消 <windows.h> 可能定义的冲突宏
#ifdef NOERROR
#undef NOERROR
#endif
#ifdef IN
#undef IN
#endif

// ============================================================================
// DNS 资源记录类型（RFC 1035 第 3.2.2 节）
// 每个枚举值对应 DNS 协议报文中的 TYPE 字段（16 位无符号整数）
// ============================================================================
enum class RRType : uint16_t {
    A     = 1,    // IPv4 地址 —— 将域名映射为 32 位 IPv4 地址
    NS    = 2,    // 权威名称服务器 —— 指定该域名的授权 DNS 服务器
    CNAME = 5,    // 规范名称 / 别名 —— 将一个域名指向另一个域名
    SOA   = 6,    // 授权起始 —— 定义域的管理信息与区域传输参数
    PTR   = 12,   // 指针记录 —— 反向 DNS，将 IP 地址映射为域名
    MX    = 15,   // 邮件交换 —— 指定该域名负责接收邮件的服务器
    TXT   = 16,   // 文本记录 —— 存储任意文本（常用于 SPF/DKIM 等验证）
    AAAA  = 28,   // IPv6 地址 —— 将域名映射为 128 位 IPv6 地址
    SRV   = 33,   // 服务定位 —— 指定特定服务的主机名和端口
    ANY   = 255   // 任意类型 —— 查询时请求返回所有类型的记录
};

// ============================================================================
// DNS 记录类（RFC 1035 第 3.2.4 节）
// 绝大多数场景下使用 IN（Internet），代表互联网类
// ============================================================================
enum class RRClass : uint16_t {
    IN = 1   // Internet 类 —— 互联网地址类，QCLASS 字段的标准值
};

// ============================================================================
// DNS 响应码（RFC 1035 第 4.1.1 节）
// 置于 DNS 报文头部 FLAGS 字段的低 4 位，表示查询结果状态
// ============================================================================
enum class RCode : uint8_t {
    NOERROR   = 0,   // 成功 —— 查询正常完成，回答有效
    FORMERR   = 1,   // 格式错误 —— 服务器无法解析请求报文
    SERVFAIL  = 2,   // 服务器失败 —— 内部错误导致无法完成查询
    NXDOMAIN  = 3,   // 域名不存在 —— 查询的域名在 DNS 体系中不存在
    NOTIMP    = 4,   // 未实现 —— 服务器不支持该类型的查询
    REFUSED   = 5    // 拒绝 —— 服务器由于策略原因拒绝回答
};
